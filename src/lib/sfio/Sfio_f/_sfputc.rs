#[no_mangle]
pub unsafe extern "C" fn sfputc(mut f: *mut Sfio_t, mut c: libc::c_int) -> libc::c_int {
    return if (*f).next >= (*f).endw {
        _sfflsbuf(f, c as libc::c_uchar as libc::c_int)
    } else {
        let ref mut fresh0 = (*f).next;
        let fresh1 = *fresh0;
        *fresh0 = (*fresh0).offset(1);
        *fresh1 = c as libc::c_uchar;
        *fresh1 as libc::c_int
    };
}
